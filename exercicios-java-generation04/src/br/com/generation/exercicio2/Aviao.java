package br.com.generation.exercicio2;

public class Aviao {
		
		private String AviaoCia;
		private String AviaoVoos;
		private int AviaoPassageiros;
		
		public void setAviao(String AviaoCia) {
			this.AviaoCia = AviaoCia;
		}
		
		public String getAviao() {
			return AviaoCia;
		}
		
		public void setVoos(String AviaoVoos) {
			this.AviaoVoos = AviaoVoos;
		}
		
		public String getVoos() {
			return AviaoVoos;
		}
		
		public void setPassageiros(int AviaoPassageiros) {
			this.AviaoPassageiros = AviaoPassageiros;
		}
		
		public int getPassageiros() {
			return AviaoPassageiros;
		}
}
